package entity;

public class TeacherIndex {
	private String TNo;
	private String TN;
	private String DisciplineNum;
	private String SecondaryNum;
	private int IndexNo;
	public String getTNo() {
		return TNo;
	}
	public void setTNo(String tNo) {
		TNo = tNo;
	}
	public String getTN() {
		return TN;
	}
	public void setTN(String tN) {
		TN = tN;
	}
	public String getDisciplineNum() {
		return DisciplineNum;
	}
	public void setDisciplineNum(String disciplineNum) {
		DisciplineNum = disciplineNum;
	}
	public String getSecondaryNum() {
		return SecondaryNum;
	}
	public void setSecondaryNum(String secondaryNum) {
		SecondaryNum = secondaryNum;
	}
	public int getIndexNo() {
		return IndexNo;
	}
	public void setIndexNo(int indexNo) {
		IndexNo = indexNo;
	}
	
	

}
