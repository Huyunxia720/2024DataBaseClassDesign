package entity;

public class SecStudent {
    private String examNo;
    private String secSN;
    private String sex;
    private String sNumber;
    private String emergenNumber;
    private String sCategory;
    private String undergraSchool;
    private String undergraTime;

    // Getters and Setters
    public String getExamNo() {
        return examNo;
    }

    public void setExamNo(String examNo) {
        this.examNo = examNo;
    }

    public String getSecSN() {
        return secSN;
    }

    public void setSecSN(String secSN) {
        this.secSN = secSN;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public String getSNumber() {
        return sNumber;
    }

    public void setSNumber(String sNumber) {
        this.sNumber = sNumber;
    }

    public String getEmergenNumber() {
        return emergenNumber;
    }

    public void setEmergenNumber(String emergenNumber) {
        this.emergenNumber = emergenNumber;
    }

    public String getSCategory() {
        return sCategory;
    }

    public void setSCategory(String sCategory) {
        this.sCategory = sCategory;
    }

    public String getUndergraSchool() {
        return undergraSchool;
    }

    public void setUndergraSchool(String undergraSchool) {
        this.undergraSchool = undergraSchool;
    }

    public String getUndergraTime() {
        return undergraTime;
    }

    public void setUndergraTime(String undergraTime) {
        this.undergraTime = undergraTime;
    }
}
