package entity;

public class SecTInf {
    private String examNo;
    private double secScore;
    private String secTime;
    private String secPlace;
    private double profTest;
    private double interview;
    private double overallScore;
    private double languageScore;

    // Getters and Setters
    public String getExamNo() {
        return examNo;
    }

    public void setExamNo(String examNo) {
        this.examNo = examNo;
    }

    public double getSecScore() {
        return secScore;
    }

    public void setSecScore(double secScore) {
        this.secScore = secScore;
    }

    public String getSecTime() {
        return secTime;
    }

    public void setSecTime(String secTime) {
        this.secTime = secTime;
    }

    public String getSecPlace() {
        return secPlace;
    }

    public void setSecPlace(String secPlace) {
        this.secPlace = secPlace;
    }

    public double getProfTest() {
        return profTest;
    }

    public void setProfTest(double profTest) {
        this.profTest = profTest;
    }

    public double getInterview() {
        return interview;
    }

    public void setInterview(double interview) {
        this.interview = interview;
    }

    public double getOverallScore() {
        return overallScore;
    }

    public void setOverallScore(double overallScore) {
        this.overallScore = overallScore;
    }

    public double getLanguageScore() {
        return languageScore;
    }

    public void setLanguageScore(double languageScore) {
        this.languageScore = languageScore;
    }
}