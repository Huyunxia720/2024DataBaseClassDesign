package entity;

public class SecDiscipline {
    private String disciplineNum;
    private String secondaryNum;
    private String preSubjects;
    private String secOverview;
    private String secDisName;

    // Getters and Setters
    public String getDisciplineNum() {
        return disciplineNum;
    }

    public void setDisciplineNum(String disciplineNum) {
        this.disciplineNum = disciplineNum;
    }

    public String getSecondaryNum() {
        return secondaryNum;
    }

    public void setSecondaryNum(String secondaryNum) {
        this.secondaryNum = secondaryNum;
    }

    public String getPreSubjects() {
        return preSubjects;
    }

    public void setPreSubjects(String preSubjects) {
        this.preSubjects = preSubjects;
    }

    public String getSecOverview() {
        return secOverview;
    }

    public void setSecOverview(String secOverview) {
        this.secOverview = secOverview;
    }

    public String getSecDisName() {
        return secDisName;
    }

    public void setSecDisName(String secDisName) {
        this.secDisName = secDisName;
    }
}